public class StringDistancePair {
    private String string;
    private int distance;

    public StringDistancePair(String string, int distance) {
        this.string = string;
        this.distance = distance;
    }

    public String getString() {
        return string;
    }

    public int getDistance() {
        return distance;
    }
}
